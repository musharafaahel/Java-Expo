package Basics;

public class DataTypes {
	public static void main(String[] args) {
		System.out.println("----Data Types---- ");
		/*
		 * 1.Numbers
		 * 2.Floating point numbers
		 * 3.Character
		 * 4.Boolean 
		 * 
		 */
		System.out.println("Basic Datatypes");
		System.out.println("1.Numbers\n"
				+ "2.Floating point numbers\n"
				+ "3.Character\n"
				+ "4.Boolean ");
		// \n --refers to new line
		
		//Numbers - (byte, short, int, long)
		byte b = 100;
		short s = 32000;
		int i = 2147483647;
		long l = 9223372036854775807L;
		
		//Floating Point - (float, double)
		float f = 10.233F;
		double d = 10.2333333333333333;
		
		//Character - (char)
		char c = 'a';
		char a = 97; //ASCII - American Standard Code for Information Interchange
		
		//Boolean - (boolean)
		boolean bb = true;
		bb =false;
		
		
		
	}
}
