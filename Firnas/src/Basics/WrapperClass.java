package Basics;

public class WrapperClass {
	public static void main(String[] args) {
		/* 
		 * Complex types of primitive datatypes are known as wrapper class.
		 * 
		 * Advantages :- 
		 *  1. They have inbuilt methods available
		 *  2. Best for performance
		 *  
		 * Disadvantages :-
		 * 	1.Always 2 objects will be created automatically
		 * 	2.Occupies More Memory
		 * 	3.Performance will decrease
		 * 	
		 * 
		 *  byte --> Byte
		 *  short --> Short
		 *  int --> Integer
		 *  long --> Long
		 *  
		 *  float --> Float
		 *  double --> Double
		 *  
		 *  char --> Character
		 *  
		 *  boolean -->Boolean
		 *  
		 *  string --> String
		 * 
		 */
		
		Byte b  = 100;
		Short s = 32000;
		Integer i = 2147483647;
		Long l = 9_223_372_036_854_775_807L;
		
		Float f = 10.23F;
		Double d = 10.233333333333;
		
		Character c = 'A';
		
		Boolean bb = true;
		
		
		
	}
}
